class SBurn():
    @staticmethod
    def get_instance(xsize, ysize, fps=30):
        return SBurn(xsize, ysize, fps)
    
    def get_frame(self):
        self.iter_direction()
        
        self.set_frame(self.bitmaps[int(self.state)])
        
        if self.order_increase:
            self.state = (self.state + 0.02)
        else:
            self.state = (self.state - 0.02)
        return self.frame

    # Iterate from first to last, then from last to first
    def iter_direction(self):
        if self.state >= len(self.bitmaps):
            self.order_increase = False
            self.state = len(self.bitmaps) - 1
        
        if self.state < 0:
            self.order_increase = True
            self.state = 1
    
    def set_frame(self, bitmap):
        for x in range(28):
            for y in range(14):
                if bitmap[y][x] == "1":
                    self.frame[x][y] = self.color_main
                elif bitmap[y][x] == "2":
                    self.frame[x][y] = self.color_fire_outer
                elif bitmap[y][x] == "3":
                    self.frame[x][y] = self.color_fire_inner
                elif bitmap[y][x] == "4":
                    self.frame[x][y] = self.color_fire_main
                else:
                    self.frame[x][y] = (0,0,0)


